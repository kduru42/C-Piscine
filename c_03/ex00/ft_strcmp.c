int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	c;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			c = s1[i] - s2[i];
			return (c);
		}
		i++;
	}
	return (s1[i] - s2[i]);
}	
