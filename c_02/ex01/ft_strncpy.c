char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (dest[n] != '\0')
	{
		dest[n] = '\0';
		n++;
	}
	return (dest);
}
